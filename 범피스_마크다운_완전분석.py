def 콜론처리(배열):
    """콜론이 없는 줄 → '엔터:엔터' 로 교체 (빈 줄 = 줄바꿈)"""
    결과 = []
    for 아이템 in 배열:
        if ':' not in 아이템:
            결과.append('엔터:엔터')
        else:
            결과.append(아이템)
    return 결과


def 엔터처리(배열):
    """내부 배열이 [''] 이면 ['엔터','엔터'] 로 교체"""
    결과 = []
    for 아이템 in 배열:
        if 아이템 == ['']:
            결과.append(['엔터', '엔터'])
        else:
            결과.append(아이템)
    return 결과


def 키워드처리(배열):
    """#오늘날짜 → 실제 날짜 치환 (YYYYMMDD)"""
    from datetime import datetime
    return [
        [s.replace('#오늘날짜', datetime.today().strftime('%Y%m%d')) for s in 내부배열]
        for 내부배열 in 배열
    ]


def 특수문자제거(배열):
    """키워드(index 0)에서 한글/공백 외 문자 제거; 내용(index 1)은 유지"""
    import re
    패턴 = re.compile(r'[^가-힣\s]')
    return [[패턴.sub('', 아이템[0]), 아이템[1]] for 아이템 in 배열]


def 배열공백제거(배열):
    """모든 문자열 strip()"""
    return [[s.strip() for s in 내부배열] for 내부배열 in 배열]


def 마크다운읽기(self):
    """
    블록 선택 텍스트 → [[키워드, 내용], ...] 배열 반환

    처리 순서:
      1) 블록텍스트() 호출 → raw 텍스트
      2) \\r\\n 으로 줄 분리
      3) 콜론처리: 콜론 없는 줄 → '엔터:엔터'
      4) [s.split(':', 1) for s in 줄나누기]
      5) 엔터처리: [''] → ['엔터','엔터']
      6) 키워드처리: #오늘날짜 치환
      7) 특수문자제거: 키워드 정제
      8) 배열공백제거: strip
    """
    블록내용 = self.블록텍스트()
    줄나누기 = 블록내용.split('\r\n')
    줄나누기 = 콜론처리(줄나누기)
    콜론나누기 = [s.split(':', 1) for s in 줄나누기]
    return 배열공백제거(특수문자제거(키워드처리(엔터처리(콜론나누기))))


def 임시변환기(텍스트):
    """'원N' → 원문자(①②...) 유니코드 변환  (9312='①')"""
    for i in range(1, 11):
        텍스트 = 텍스트.replace(f'원{i}', chr(9311 + i))
    return 텍스트


