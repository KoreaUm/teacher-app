def 순화검색(텍스트):
    """
    텍스트(선택된 단어)를 받아 순화어를 반환

    Args:
        텍스트 (str): 검색할 단어 또는 구절

    Returns:
        list: [원문, 추천순화어, 설명] 또는 None (없을 경우)

    내부 로직:
        - 공백 없으면 → 단어순화1 검색
        - 공백 1개    → 단어순화2 검색
        - 공백 2개 이상 → 단어순화3 검색
    """
    공백수 = 텍스트.count(' ')
    if 공백수 == 0:
        딕셔너리 = 단어순화1
    elif 공백수 == 1:
        딕셔너리 = {}  # 단어순화2 (별도 사전)
    else:
        딕셔너리 = {}  # 단어순화3 (별도 사전)

    if 텍스트 in 딕셔너리:
        결과 = 딕셔너리[텍스트]
        return [텍스트, 결과[0], 결과[1] if len(결과) > 1 else '']
    return None


